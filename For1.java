
public class For1 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        for (int j = 1; j <= 10; j++) {
            if (j == 5) {
                System.out.println("Hey du hast mitch gefunden " + j);
            }
        }
    }
}